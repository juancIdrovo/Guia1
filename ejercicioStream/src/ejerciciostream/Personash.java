    public void lista() {
        try {

            List<String> lineas = Files.readAllLines(Paths.get("C:\\Users\\tatip\\Desktop\\listaPersonas.txt"));
            lineas.stream().map(linea -> linea.split(" ")).filter(datos -> datos.length >= 3).forEach(datos -> {
                String nombre = datos[0];
                String apellido1 = datos[1];
                String apellido2 = datos[2];

                Persona persona = new Persona();
                persona.setNombre(nombre);
                persona.setApellido1(apellido1);
                persona.setApellido2(apellido2);

                personas.add(persona);

            });

        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    public void tiempoEjecucion() {

        long tiempoInicio = System.currentTimeMillis();
        
        long tiempoFin = System.currentTimeMillis();

        long tiempoTranscurrido = (tiempoFin - tiempoInicio) / 1000;

        String tiempoTranscurridoStg = String.valueOf(tiempoInicio);

        txtTiempo.setText(tiempoTranscurridoStg);

    }

