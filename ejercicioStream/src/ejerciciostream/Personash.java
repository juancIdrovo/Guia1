    private void btnBuscarPersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPersonaActionPerformed

        lista();

        String buscaNom = txtBuscaNombre.getText();
        String buscaApe1 = txtApellidoP.getText();
        String buscaApe2 = txtApellidoM.getText();

        Optional<Persona> personaEncontrada = personas.stream().filter(persona -> persona.getNombre().equals(buscaNom) && persona.getApellido1().equals(buscaApe1)
                && persona.getApellido2().equals(buscaApe2)).findFirst();

        personaEncontrada.ifPresentOrElse(persona -> txtResultadoBusqueda.setText("Persona encontrada: " + persona.getNombre() + " " + persona.getApellido1() + " "
                + persona.getApellido2()), () -> txtResultadoBusqueda.setText("La persona que buscas no existe."));
        
          tiempoEjecucion();

    }//GEN-LAST:event_btnBuscarPersonaActionPerformed

