    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Factura f = new Factura("ordenador", 1200);
        Factura f2 = new Factura("movil", 300);
        Factura f3 = new Factura("impresora", 200);
        Factura f4 = new Factura("imac", 1500);

        lista.add(f);
        lista.add(f2);
        lista.add(f3);
        lista.add(f4);

        Predicate<Factura> predicado = new Predicate<Factura>() {

            String imp = txtImporte1.getText();

            @Override
            public boolean test(Factura f) {

                txtIteracion.setText(" iteracion ");

                double imp2 = Double.valueOf(imp);

                return f.getImporte() > imp2;

            }

        };

        Factura facturaFiltro = lista.stream().filter(predicado).findFirst().get();

        txtUnica.setText("UNICA" + String.valueOf(facturaFiltro.getImporte()));

    }//GEN-LAST:event_jButton1ActionPerformed

